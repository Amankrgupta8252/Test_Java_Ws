public class java_method_parameters {
    public static void myMethod(String name, int age) {
        System.out.println("name: " + name + ", age: " + age);
    }

    public static void main(String[] args){
        myMethod("Aman Kumar Gupta", 20);
        myMethod("Rahul Kumar", 22);
        myMethod("Anish Kumar", 21);
        myMethod("Alok Kumar", 23);
        myMethod("Ansh Kumar", 24);
        myMethod("Ankita Kumari", 25);
        myMethod("Ankush Kumar", 26);

    }
    
}
