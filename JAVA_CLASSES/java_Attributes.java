public class java_Attributes {
    String fname = "Aman";
    String lname = "Gupta";
    int age = 20;

    public static void main(String[] args) {
        java_Attributes myObj = new java_Attributes();
        System.out.println("Full Name: " + myObj.fname + " " + myObj.lname); 
        System.out.println("Age: " + myObj.age);
    }
}
