public class java_declare_variables {
    public static void main(String[] args) {
        //Instead of writing:
        int x = 5;
        int y = 6;
        int z = 50;
        System.out.println(x + y + z);
        //You can simply write:
        int a = 5, b = 6, c = 50;
        System.out.println(a + b + c);

    }
}
