public class java_math {
    public static void main(String[] args) {
        System.out.println("Max"+Math.max(14,85)); // max

        System.out.println("Min"+Math.min(10,50)); // min

        System.out.println("square"+Math.sqrt(4846)); //square

        System.out.println("absolute"+Math.abs(-24654.1654));// absolute

        System.out.println("random"+Math.random()); // random

        int randomNum = (int)(Math.random()*101);
        System.out.println(randomNum);
    }
}
